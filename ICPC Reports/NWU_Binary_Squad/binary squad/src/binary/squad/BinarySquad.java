
package binary.squad;

import java.util.Scanner;

public class BinarySquad {

    public static void main(String[] args) {

       Scanner in=new Scanner(System.in);
       int n=in.nextInt();
       int a=in.nextInt();
       int b=in.nextInt();
       int z=n-a;
       System.out.print(z);
       System.out.print(" ");
       System.out.print(z-b);
     
    
}
}
